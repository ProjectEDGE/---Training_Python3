
numero1 =   10  # Variable numero1 con un valor asignado.
numero2 =   20  # Variable numero2 con un valor asignado.

print(numero1 == numero2)   # Comparador Igual a        /   10 == 20    False
print(numero1 != numero2)   # Comparador Distinto       /   10 != 20    True
print(numero1 <  numero2)   # Comparador Menos          /   10 < 20     True
print(numero1 >  numero2)   # Comparador Mayor          /   10 > 20     False
print(numero1 <= numero2)   # Comparador Menor o Igual  /   10 <= 20    True
print(numero1 >= numero2)   # Comparador Mayor o Igual  /   10 >= 20    False





